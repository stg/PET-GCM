void mod_data_read( u8* pDest, u24 nof_bytes ) {
  if( pDest )
    while( nof_bytes-- )
      if( pData >= mod_data + sizeof( mod_data ) - 1 )
        *pDest++ = 0;
      else
        *pDest++ = *pData++;
  else
    pData += nof_bytes;
}
